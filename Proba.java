public class Proba {
	public int joc;
	public int banda;
	public int adunare;
	
	public Proba(int joc,int banda,int adunare) {
		this.joc = joc;
		this.banda = banda;
		this.adunare = adunare;
	}
	
	public int getJoc() {
		return joc;
	}

	public int getBanda() {
		return banda;
	}

	public int getAdunare() {
		return adunare;
	}
}
