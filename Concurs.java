public class Concurs {
	public int games;
	public int comics;
	public int pozitie;
	
	public Concurs(int games,int comics,int pozitie) {
		this.games = games;
		this.comics = comics;
		this.pozitie = pozitie;
	}
	
	public int getGames() {
		return games;
	}

	public int getComics() {
		return comics;
	}

	public int getPozitie() {
		return pozitie;
	}
}
